/*
* ピンを計算して、ラグのあるプレイヤーに対して巻き戻し処理を行うかを決定する
* ラグが高いプレイヤーに警告アイコンの表示
*/
void ABlasterPlayerController::CheckPing(float DeltaTime)
{
	if (HasAuthority()) return;
	HighPingRunningTime += DeltaTime;
	if (HighPingRunningTime > CheckPingFrequency)
	{
		PlayerState = PlayerState == nullptr ? GetPlayerState<APlayerState>() : PlayerState;
		if (PlayerState)
		{
			//UE_LOG(LogTemp, Warning, TEXT("PlayerState->GetPing() * 4 : %d"), PlayerState->GetPing() * 4);
			if (PlayerState->GetPing() * 4 > HighPingThreshold) // ping is compressed; it's actually ping / 4
			{
				HighPingWarning();
				PingAnimationRunningTime = 0.f;
				ServerReportPingStatus(true);
			}
			else
			{
				ServerReportPingStatus(false);
			}
			float CurrentPing = PlayerState->GetPingInMilliseconds();
			if (BlasterHUD && BlasterHUD->CharacterOverlay && BlasterHUD->CharacterOverlay->PingAmountText)
			{
				FString PingText = FString::Printf(TEXT("%d"), FMath::FloorToInt(CurrentPing));
				BlasterHUD->CharacterOverlay->PingAmountText->SetText(FText::FromString(PingText));
			}
		}
		HighPingRunningTime = 0.f;
	}
	bool bHighPingAnimationPlaying =
		BlasterHUD && BlasterHUD->CharacterOverlay &&
		BlasterHUD->CharacterOverlay->HighPingAnimation &&
		BlasterHUD->CharacterOverlay->IsAnimationPlaying(BlasterHUD->CharacterOverlay->HighPingAnimation);
	if (bHighPingAnimationPlaying)
	{
		PingAnimationRunningTime += DeltaTime;
		if (PingAnimationRunningTime > HighPingDuration)
		{
			StopHighPingWarning();
		}
	}

	// Test Code
	/*
	float CurrentPing = PlayerState->GetPingInMilliseconds();
	if (BlasterHUD && BlasterHUD->CharacterOverlay && BlasterHUD->CharacterOverlay->PingAmountText)
	{
		FString PingText = FString::Printf(TEXT("%d"), FMath::FloorToInt(CurrentPing));
		BlasterHUD->CharacterOverlay->PingAmountText->SetText(FText::FromString(PingText));
	}*/
}

